package model;

/**
 *
 * @author win
 */
public class MyCircle {
    private int radius, x,y;
    private String color;

    public MyCircle() {
    }

    public MyCircle(int radius, int x, int y, String color) {
        this.radius = radius;
        this.x = x;
        this.y = y;
        this.color = color;
    }
    

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
    
}
